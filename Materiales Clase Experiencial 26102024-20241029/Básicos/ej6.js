// Define una función llamada "primerElemento" que toma un parámetro "arr" (un array)
function primerElemento(arr) {
    // Devuelve el primer elemento del array accediendo al índice 0
    return arr[0];
}

// Llama a la función con un array y muestra el resultado en la consola
console.log(primerElemento([10, 20, 30])); // Salida: 10
