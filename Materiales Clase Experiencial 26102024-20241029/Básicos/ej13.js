// Define una función llamada "invertirArray" que toma un parámetro "arr" (un array)
function invertirArray(arr) {
    // Usa el método "reverse" para invertir el array y devolver el resultado
    return arr.reverse();
}

// Llama a la función con un array y muestra el resultado en la consola
console.log(invertirArray([1, 2, 3, 4])); // Salida: [4, 3, 2, 1]
