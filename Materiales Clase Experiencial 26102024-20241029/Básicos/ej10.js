// Define una función llamada "calcularCuadrado" que toma un parámetro "num" (un número)
function calcularCuadrado(num) {
    // Retorna el cuadrado de "num" multiplicándolo por sí mismo
    //return num * num;
    return Math.pow(num,2);
}

// Llama a la función con un número y muestra el resultado en la consola
console.log(calcularCuadrado(4)); // Salida: 16
console.log(calcularCuadrado(7)); // Salida: 49
