// Define una función llamada "concatenarCadenas" que toma dos parámetros, "cadena1" y "cadena2" (ambas son cadenas de texto)
function concatenarCadenas(cadena1, cadena2) {
    // Retorna la concatenación de "cadena1" y "cadena2" usando el operador "+"
    return cadena1 + cadena2;
}

// Llama a la función con dos cadenas y muestra el resultado en la consola
console.log(concatenarCadenas("Hola, ", "mundo")); // Salida: "Hola, mundo"
console.log(concatenarCadenas("Luz", "Álvarez")); // Cuidado con los espacios
console.log(concatenarCadenas("Luz ", "Álvarez")); 
