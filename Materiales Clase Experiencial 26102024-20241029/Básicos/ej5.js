// Define una función llamada "contarCaracteres" que toma un parámetro "cadena" (una cadena de texto)
function contarCaracteres(cadena) {
    // Retorna la longitud de la cadena, que es el número de caracteres, usando la propiedad "length"
    return cadena.length;
}

// Llama a la función con una cadena y muestra el resultado en la consola
console.log(contarCaracteres("JavaScript")); // Salida: 10
console.log(contarCaracteres("JavaScript ")); // Salida: 11
