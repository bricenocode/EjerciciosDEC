// Define una función llamada "ultimoElemento" que toma un parámetro "arr" (un array)
function ultimoElemento(arr) {
    // Devuelve el último elemento del array utilizando el índice de la longitud del array menos 1
    return arr[arr.length - 1];
}
// Llama a la función con un array y muestra el resultado en la consola
console.log(ultimoElemento([1, 2, 3, 4])); // Salida: 4
