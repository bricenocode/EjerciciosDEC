// Define una función llamada "valorAbsoluto" que toma un parámetro "num" (un número)
function valorAbsoluto(num) {
    // Utiliza el método "Math.abs" para obtener el valor absoluto del número y lo devuelve
    return Math.abs(num);
}

// Llama a la función con un número y muestra el resultado en la consola
console.log(valorAbsoluto(-10)); // Salida: 10
console.log(valorAbsoluto(5));   // Salida: 5
