// Define una función llamada "convertirMayusculas" que toma un parámetro "cadena" (una cadena de texto)
function convertirMayusculas(cadena) {
    // Convierte "cadena" a mayúsculas usando el método "toUpperCase" y devuelve el resultado
    return cadena.toUpperCase();
}
// Llama a la función con una cadena y muestra el resultado en la consola
console.log(convertirMayusculas("hola")); // Salida: "HOLA"
